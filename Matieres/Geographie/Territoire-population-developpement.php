<h5 class="sous_titre" id="definition">Définitions</h6>
<div class="content">
<p>
<a class="surlignage" id="accroissement-naturel">Accroissement naturel :</a> différence de taux de natalité et de mortalité. Il est évalué en pourcentage
et peut être positif nul et négatif.
<br><br>
<a id="seuil-renouvellement" class="surlignage">Seuil de renouvellement :</a> Niveau moyen de fécondité pour avoir une population constante (2.1 enfant par femmes).
<br><br>
<a class="surlignage">Transition démographique :</a> passage d'un régime démographique traditionnel où la fécondité
et la mortalité sont élevées et s'équilibrent à peu près à un régime ou la natalité et la mortalité sont faibles et s'équilibrent également.
</p>
