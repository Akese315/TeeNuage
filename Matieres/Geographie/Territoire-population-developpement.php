<h2 class="sous_titre" id="intro">Introduction</h2>
<div class="content">
<p>
La population mondiale a fortement augmenté depuis les années 1950, passant de 2.5milliard à plus
de 7.5 milliards d'habitant ajourd'hui. Et les projections prévoient plus de 9.5 milliard d'habitants en 2050.
Mais cette évolution démographique n'est pas la même partout, avec des pays du Nord Japon dont la population
tend à stagner voir à diminuer.<a class ="surlignage" onclick ="scrollFunction('accroissement-naturel')"> Accroissement naturel </a>nul ou négatif, tandis que d'autres
parties du monde comme l'Afrique voient leur population fortement augmenter(l'inde et la chine étant les deux pays les plus peuplés au monde).

</p>
</div>
<h3 class="sous_titre" id="croissance-demographique-afrique">La croissance démographique en Afrique</h3>
<div class="content">
<p>
L'Afrique subsaharienne fait l'objet d'une croissance de la population très élevée. Cette forte démographie 
s'explique par la culture, le niveau de vie des habitants ou encore . Cependant une transition démographique vient juste
d'être entamée ayant pour objectif de réduire cette croissance :

<ul class="list">Baisse de la fécondité :<br><br>
<li>Hausse de la contraception</li>
<li>recul de l'âge moyen du mariage</li>
<li>recul de la polygamie</li>
</ul>

<ul class="list">Baisse de la mortalité :<br><br>
<li>recul notable de la mortalité infantile</li>
<li>hausse de l'espérence de vie</li>
<li>recul de certaines maladies comme le sida</li>
</ul><br><br>
Cependant cette transition est encore loin d'être terminée. Une fécondité encore très élevée
(au moins 3,5 enfants par femme dans plusieurs pays)

<ul class="list">
<li>Forte demande d'enfant</li>
<li>Proportion encore non négligeable de mariages d'adolescentes.</li>
<li>Une mortalité encore trop présente</li>
<li>Menace encore persitante de maladies comme le sida</li>
<li>Encore une forte mortalité maternelle</li>
<li>Progression de certaines maladies</li>

</ul>

On peut donc en conclure que malgré ses efforts, l'Afrique doit encore garantir encore plus sa population
en hausse de la sécurité, de l'emploie, des systèmes de santé et d'éducation et plus généralement, favoriser le 
dévloppement en luttant contre la pauvreté. D'autant plus que cette hausse de la population pousse ses habitants
et partir et à migrer vers d'autres pays.
</p>
</div>
<h4 class="sous_titre" id="japon-vieillissement">Le vieillissement du Japon</h4>
<div class="content">
<p>
Divers moyens sont utilisés pour réaliser une transition démographique exemple : la contraception ou Encore
la politique de l'enfant unique en Chine. Néanmoins il existe aussi des pays en situation de post-transition comme le Japon.
<br><br>
Tout d'abord, le Japon est un pays qui possède beaucoup de règles concernant le mariage,procréation et le rôle de l'homme / femme.
Par exemple :
<ul class="list">
<li>Un couple ne peut se marier avant d'avoir un revenue stable.</li><br>
<li>Si un couple donne naissance à un/des enfant(s), la femme doit rester à la maison pour l'éduquer 
et l'homme doit travailler dur. Ainsi si la femme ne travaille pas il y a moins de revenue et l'arrivée d'un enfant complique financièrement le couple.</li>

</ul>
<br><br>
<img id="img1"src ="https://www.nippon.com/fr/ncommon/contents/japan-data/145904/145904.jpg"><br><br>
<label><em>Les femmes aux fourneaux : rien de plus normal pour la majeure partie des hommes japonais : d'après </em><a target = "_blank" class="surlignage" href="https://www.nippon.com/fr/japan-data/h00499/">Nippon.com</a></label>
<br><br>
Toutes ces règles contraignent et ne favorisent pas la croissance démographique. Au fil des années la population vieilie et peu d'enfants
naissent. Cela veut dire que beaucoup d'individu entrent en retraite donc énormément de perte de main d'oeuvre. Et
comme peu de jeunes naissent, il y a peu de futur main d'oeuvre et l'Etat gagne moins d'argent. Au final, payer la retraite des personnes agées s'annonce de plus en plus difficile.
Le gouvernement a bien notifié ce problème et des solutions se sont mis en place :
    <ul class="list">
<li>Hausse du nombre d’emplois grâce aux services à destination des seniors.</li><br>
<li>Réformes structurelles</li><br>
<li>Politiques favorables à la natalité avec par exemple la gratuité des places en crèche </li><br>
<li>Robots dans les usines pour combler le manque de maind’œuvre </li><br>

    </ul>

    <br><br>
   <a class="surlignage"> Pour conclure :</a>
    <br><br>

De nombreux pays dans le monde, en particuliers ceux du Nord (Japon, France, Allemagne) présentent aujourd'hui
des phénomènes de vieillissement de leur population. Se pose alors des enjeux économiqes et sociaux comme par exemple le 
<a class ="surlignage" onclick ="scrollFunction('seuil-renouvellement')">seuil de renouvellement</a>
 de la population, le paiement des retraite ou l'offre d'emploie.
 <br><br>
 Face à cela et en fonction des pays, plusieurs réponses sont possibles comme l'immigrations ou la robotisation tandis que se développent de nouveaux secteurs
 de l'économie lié aux personnes agées.
</p>
