<h3 class="sous_titre" id="Etude-de-cas">Etude de cas :</h3>
<div class="content">
<p>
Comparaison des modèles de développement et des conséquences entre deux pays la Russie et l'Inde.
<br><br>
Il existe plusieurs façon pour un pays de se développer, en fonction des atouts que le pays possède :
<ul class="list">
<li>Par exemple, les pays peuvent miser sur l'exploitation de leurs ressources énergétique tel que le pétrol de la 
Russie qui est exportés. On nome cela une économie de rente qui est donc basé sur l'exportation des ressources naturelles.</li>
<br><br>
<li>Et au contraire, les pays peuvent miser sur les études et les compétences intellectuelles des individus comme
la Corée du sud avec leur études très dures.</li>
<br><br> 
<li>Ou alors comme l'Inde ou la Chine, miser sur la main d'oeuvre et une économie liberalisée pour attirer les 
investisseurs.</li>

</ul>
<br><br>
Cependant des innégalités augmentent, notamment dans les pays émergents comme l'inde, qui se développe mais qui est
aussi très marquée par ces inégalités. L'éducation peut participer à une réduction des inégalités.
</p>

