<h5 class="sous_titre">Définitions</h5>
<div class="content">
<p>
<a class="surlignage">Economie de rente :</a> Lorsque l'essentiel des revenus d'un pays provient de la vente des ressource 
de ce dernier par exemple : le pétrol. <br><br>
<a class="surlignage">Pays émergents :</a> pays connaissant un fort et rapide développement, même si ce dernier
est encore inférieur à celui des pays le splus riches. Si on assiste à une hausse de la qualité de vie (ex : naissance d'une
classe moyenne), perdurent de fortes inégalités entre les habitants. <br><br>
<a class="surlignage">IDH :</a> (Indice de Développement Humain) indicateur du niveau de développement d'un pays allant de 0 (pas développé) à
1 (très développé) et prenant en compte différents facteurs (espérence de vie, revenus par habitants, etc...).
