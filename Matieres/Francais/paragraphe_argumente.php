<h2 class = "sous_titre" id ="introduction"> L'introduction</h2>
<div class = "content">
<p>
Au début d'un paragraphe argumenté, il faut rédigé une courte introduction qui comprends les éléments suivants : <br> <br>
<a class = "surlignage">Une phrase qui précise le contexte (de quoi parle t'on, le thème ) </a>.
</p>
<br><br>
<p>
Exemple : <em>"Un certain nombre de villes sont célèbres pour leur taille spectaculaire"</em> <br> <br>
Dans ce début de texte on pourrait déduire que le thème de cette phrase est <em> "La croissance urbaine" </em>.

</p><br>
<p> <a class = "surlignage"> Une deuxième phrase pour poser une problématique </a> <br> <br>

<a class = "surlignage">définition problématique </a>=  Ensemble des questions posées par un sujet précis. <br> <br>

Pour poser une problématique, soit vous recopiez exactement l’intitulé du sujet, soit vous le reformulez sous forme
de question mais toujours en reprenant les mots exacts du sujet. Pour un sujet d’histoire, il
est souvent nécessaire de délimiter dans le temps votre sujet (précisez les dates ou années entre lesquelles il se
déroule).<br> <br>

Exemple : Quels sont les raisons de cette croissance urbaine?

</p>
<p> <a class = "surlignage">Enfin une troisième phrase pour donner le plan utilisé</a> (c'est un choix personnel dans lequel vous choisissez le plan le mieux approprié).<br> <br>
voici tout les plans que vous pouvez utiliser : <br> <br>
-<a class="surlignage">Le plan antithétique</a> est utilisé pour diviser le sujet en 2 paragraphe opposé exemple : <br><br><br> 
<ul class ="list">
<li>-Les points négatifs</li> <br>
<li>-Les points positifs</li><br> <br> 
ou encore : <br><br><br> 
<li>-Le parti de gauche (politique)</li> <br>
<li>-Le parti de droite (politique)</li><br>
</ul></p>
<p><br><br>
-<a class="surlignage">Le plan thématique</a> est utilisé pour diviser le sujet en différents thèmes : <br><br>

Exemple de sujets : les animaux <br> <br>
<ul class ="list">
<li>Les mamifères</li> <br>
<li>Les cétacés</li><br>
<li>Les insectes</li> <br>

</p>
</div>
<h3 class = "sous_titre" id ="Developpement"> Le développement </h3>
<div class = "content">
<p>
Lors du développement, il faut tout d'abord trouver les idées principales qui répondent
à la question (problématique). <br><br><br>
Ensuite vous devez faire un brouillon rapide dans lequel les idées et les arguments y figureront.

</p><br><br>
<p><a class = "surlignage">note :</a> Chaque idée représente un paragraphe et commence par un alinéa (espace de 2 gros carreaux). <br> <br>

Au tout début d'un paragraphe, doit figurer un connecteur logique (mot de liaison).<br>

Selon le type de paragraphe on emploie pas les même mot de liaisons :<br><br><br>
<a class = "surlignage">1er paragraphe</a><br> <br>
-Pour commencer<br>
-En premier lieu<br>
-Tout d'abord
-Dans un premier temps<br><br>
<a class = "surlignage">2ème paragraphe</a><br> <br>
-En second lieu<br>
-Dans un second temps<br><br>
<a class = "surlignage">paragraphe de conclusion</a><br> <br>
-Enfin<br>
-Pour conclure<br>
-En résumé<br>
-Pour finir<br><br>
</p>
<p>
Après avoir choisi son connecteur logique, il faut exposer son idée accompagné de son argument et de sa preuve ou de sa citation.
<br><br>
<a class = "surlignage"> Structure :</a>
<ul class = "list">

    <li>+ Connecteur logique (tout d'abord)</li>
    <li>+ Verbe argumentatif ex : On obeserve</li>
    <li>+ argument</li>
    <li>+ la citation ex : comme nous le montre cet extrait "..."</li>
    <li>+ ligne</li>
    <li>+ procédés littéraire (si il y'en a) et intention de l'auteur.</li>

</ul>


<br><br><a class ="surlignage">Exemple :</a> <br><br>
<a class ="fleche">Tout d'abord</a>, la lumière bleu est nocif pour la santé, <a class ="fleche">car</a> elle supprime la production de mélatonine (une hormone importante qui favorise le sommeil).
Depuis l'arrivée de la technologie(15ans environs) le français a perdu 1H 30 de sommeil. <br>
<a  target="_blank" class = "surlignage" href = "https://www.doctissimo.fr/html/psychologie/bien_dormir/articles/9406-troubles-sommeil-francais.htm">clickez ici pour voir la preuve </a> <br> <br> <br>

<a class="surlignage"> note :</a> Ne recopiez pas toute une phrase, prenez des éléments précis du texte.
</p>
</div>
<h3 class = "sous_titre" id="Conclusion"> La conclusion </h3>
<div class = "content">
<p>
Pour finir, la conclusion est un court paragraphe qui répond à la problématique et qui résume votre développement.
<br>Vous pouvez également terminer en élargissant votre sujet (ouverture). Cela permet de dire qu'un éventuel autre développement construit est réalisable.
</p>
<p>
Exemple : <br><br>
<a class ="fleche">Pour conclure</a>, l'eau est un élément indispensable pour la circulation sanguine. Le sang en est composé majoritèrement que ce soit chez les insectes, les cétacés ou les mamifères.
<a class = "fleche">Mais</a> que se passe t'il au niveau sanguin si l'eau diminue ?
</p>
