        <h2 class="sous_titre" id="Intro"> Introduction </h2>
        <div class="content">
            Tristan Corbière est un poète maudit du 19ème siècle appartenant au mouvement de du symbolisme/romantisme.
            <a class="surlignage">« Les amours jaunes »</a> est sortit en 1873 et sera son unique recueil. C'est un poème qui
            porte sur un dialogue entre deux personnes qui s'adonne à une promenade nocture. Elle va être interrompue par la rencontre insolite
            avec ce crapaud. Le poème a pour thème l'auto dérision.
            Il a 2 tercet, 2 quatrain et un vers pour la chute. 
            <br><br>
            <img src="images/crapaud.png">
        </div>
        <h3 class="sous_titre" id="plan">Le plan</h3>
        <div class="content">
            Le poème est divisé en deux mouvements :
            <ul class="list">
                <li>Les 2 premiers tercet décrivent ambiance fantastique et un chant misterieux.</li><br>
                <li>Les 2 quatrin décrivent la beauté et la laideur du crapeau. </li><br>
                <li>Puis la chute qui révèle l'identité du crapeau.</li>
            </ul>
        </div>
        <h4 class="sous_titre" id="problematique">La problématique</h4>
        <div class="content">
            Nous verrons comment Corbière parvient à faire son autoportrait.           
        </div>
        <h5 class="sous_titre" id="1er">1er mouvement : le chant</h5>
        <div class="content">
            <ul class="list">
                <li>La phrase averbale ainsi que la négation <span>« nuit sans air »</span> exprime une atmosphere d'étouffement et de pesanteur.</li><br>
                <li>La « nuit » et la « lune » dessinent un univers fantastique et mysterieux.</li><br>
                <li>Corbière crée des sensations de froideur avec les mots : <span>« plaque »</span> « métal », « découpure ».
                    L'image que nous renvoie la plaque va casser le côté naturel de la lune et de la nuit avec quelque chose d'artificiel.
                </li><br>
                <li>Les points de suspensions au début du vers 4 crée un silence qui génère une attente comme un écho : « ...un chant; comme un echo» .
                    De plus, « un chant » est en anaphore. Le tout amplifie la répétition</li><br>
                    <li>L'auteur veut associer l'idée du chant à la mort avec des mots non aeriens tel que :
                    « enterrés », « sous le massif ».
                    </li><br>
                    <li>Le massif est le seul décor végétal.</li><br>
                    <li>L'adverbe « là » répété 2 fois dans le tercet, met en proximité le crapaud et le poète qui s'identifie à lui.
                         Il fait d’ailleurs écho au « là » de « ce crapaud-là » dans le dernier vers.</li><br>
                    <li>Le crapeau se trouve dans l'ombre  « viens c'est là dans l'ombre » 
                        Comme Baudelaire, corbière utilise la lumière et l'ombre pour se positionner dans le monde : 
                        Baudelaire se voyait dans la lumière près de Dieu, tandis que Corbière à cause de sa laideur, se situe dans l'ombre.</li><br>
                    <li>Le « viens, c'est là » suppose qu'il y a une deuxième personne qui l'accompage dans cette découverte du crapaud (son mirroir).</li><br>
                    <li>Le crapaud est dévalorisé par des pronoms démonstratifs indéfinits:  <span>« ça »</span>, <span>« c' »</span>.</li><br>
                
            </ul>
        </div>
        <h6 class="sous_titre" id ="2eme">2ème mouvement : La découverte du crapeau </h6>
        <div class="content">
            <ul class="list">
                <li>Le deuxième personnage est appeuré comme le montre cette exclamation :  « Un crapaud ! » et
                    comme le confirme la didascalie :  « pourquoi cette peur ? ».
                </li><br>
                <li>
                    De plus « près de moi, ton soldat fidèle »
                    renvoie au moyen age et son cliché du preux chevalier prêt à tout pour sa princesse.
                </li><br>
                <li>
                    Le « Crapaud » symbolise un animal maudit comme le prince charmant transformé en crapaud par les sorcières… Corbière se voit en poète maudit.

                </li><br>
                <li>
                    Corbière aimerait qu'on le considère et oblige la femme à le regarder à regarder le crapaud : « vois-le ».
                </li><br>
                <li>
                    Corbière insiste sur le fait qu'il n'a pas sa place auprès de Dieu à cause de sa laideur : « tondu sans ailes »
                    Une anaphore probable à L'albatros de Baudelaire qui s'élève vers les Cieux grâce à ses longues ailes. Ici, « tondu » transmet 
                    l'idée d'humiliation et sans ailes l'incapacité à se rapprocher de Dieu.
                </li><br>
                <li>
                    L'oxymore « Le rossignol de la boue » fait allusion au poète maudit. Il a beau exceller dans son art comme le rossignol il est méprisé et 
                    reste dans la boue que Baudelaire lie directement à la saleté.
                </li><br>
                <li>
                    Le chant s'assimilant à la poésie, l'auteur fait état du manque de reconnaissance : « ...il chante ».
                </li><br>
                <li>
                    La femme ne voulait pas voir la laideur du crapaud comme les gens à l'époque n'appréciaient pas la poésie de corbière parce qu'elle était différente.
                </li><br>
                <il>
                    Malgré cela, la femme continue de crier « Horreur !! » avec un point d'exclamation en plus. Incrompréhensible aux yeux de l'auteur : « horreur pourquoi ? »
                </il><br>
                <li>
                    Corbière utilise l'alchimie poétique pour embelir l'oeil jaune du crapaud en lumière qui est l'idéal pour Baudelaire.
                </li><br>
                <li>L'auteur est exclu aussi bien du coeur de la femme que de la société. Il s'isole jusqu'à se faire passer pour mort :
                « Il s'en va, froid, sous sa pierre. ».
                </li><br>
                <li>
                    La ligne de point représente sûrement la pierre sous laquelle il se cache. Dans tout les cas, la communication avec la femme est rompue.

                </li>
            </ul>
        </div>  
        <h7 class="sous_titre" id="chute">La chute</h7>
        <div class="content">
            Le dernier vers n'est autre que la chute. Il s'agit de la révélation de son identité
            « Bonsoir, ce crapaud là c'est moi. ». Cela signifie que le poète se voit aussi laid qu'un crapaud,
            autant son art que son physique. <br><br>
            Néanmoins il donne l'image d'un crapaud polie et courtois comme le montre : « Bonsoir » et « ton soldat fidèle ».
        </div>
        <h8 class="sous_titre" id="conclusion">Conclusion</h8>
        <div class="content">
        Ce poème présente l’assimilation du poète avec le crapaud, dans un sonnet à l’envers, et montre de façon originale la <strong> dérision </strong>
        avec laquelle Corbière se considérait mais aussi comment le poète est perçu dans la société. Ce poème, autoportrait d'un poète maudit
        , peut être rapproché à <a href = "Lecture-lineaire-Albatros.php" class="surlignage">l'Albatros</a>.
