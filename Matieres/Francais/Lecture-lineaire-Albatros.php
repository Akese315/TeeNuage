        <h3 class="sous_titre" id="problematique"> Problématique </h2>
        <div class="content">
        En quoi ce petit poème constitue-t-il un apologue sur la conservation de la société humaine?
        </div>
        <h5 class="sous_titre" id="1er">1<sup>ère</sup> strophe</h5>
        <div class="content">
            <ul class="list">
            <li>Le poète souligne l'attitude des marrins avec le cadre spatio-temporel «souvent» qui montre la répétition.</li>
            <br><br>
            <li>Le complément circonstancielle de but «pour»  suggère la cruauté et la futilité des hommes.</li>
            <br><br>
            <li>Les periphrase «vastes oiseaux des mers» et «compagnons de voiyages» mettent en valeur le côté pacifique, amicale et majestueux de l'oiseau.</li>
            <br><br>
            <li>Le verbe «suivent» montre le désir de l'Albatros d'être avec les hommes.</li>
            <br><br>
            <li>L'allitération en «s» imite le glissemetn à la fois du navire et de l'oiseau.</li>
            </ul>
        </div>
        <h6 class="sous_titre" id="2eme">2<sup>ème</sup> strophe</h6>
        <div class="content">
            <ul class="list">
            <li>La périphrase méliorative : «Roi de l'azur» emplifie la superiorité de l'oiseau dans le ciel et forme une antithèse avec deux adjectif péjoratif 
                lorsqu'il est sur le sol : «maladroit et honteux».
            </li>
            <br><br>
            <li>«pitheusement» est en antithèse avec «grandes ailes blanches» qui représente la majestuosité de l'albatros. 
                Ainsi les ailes de l'Albatros se transforment en fardeaux. </li>
                <br><br>
                <li>Le champs lexcial de l'Albatros souligne la déchéance de l'Albatros.</li>
            
            </ul>
        </div>
        <h7 class="sous_titre" id="3eme">3<sup>ème</sup> strophe</h7>
        <div class="content">
            <ul class="list">
            <li>Comme précédemment, baudelaire continue de renforcer la honte de l'albatros avec «gauche et veule».</li>
            <br><br>
            <li>«lui naguère si beau» est en opposition avec «qu'il est comique et laid».</li>
            <br><br>
            <li>Les phrases exclamatives évoquent la moquerie des marins qui cherchent à l'humilier.</li>
            <br><br>
            <li>L'albatros se fait malmener par les marrins avec des violences physiques «l'un agace son bec avec un brûle gueule»</li><br><br>
            <img src="images/brule_gueule.jfif">
            <p>Un brûle gueule</p>
            <li>Les pronoms «il», «lui», «l'», montre la solitude de l'oiseau.</li>            
        </ul>
        </div>
        <h8 class="sous_titre" id="4eme">4<sup>ème</sup> strophe</h8>
        <div class="content">
            <ul class="list">
            <li>Au vers 13, Baudelaire compare directement le poète à un albatros : «Le Poète est semblable au prince des nuées»</li>
            <br><br>         
            <li>Le vers 13 et 14 montre la puissance de l'oiseau lorsqu'il est en hauteur «Qui hante les tempêtes et se rit de l'archer»<br>
                Autrement dit il se moque de la mort tout comme le poète qui grâce à ses écrits devient immortel.</li>
            <br><br>
            <li>
                Il représente la vie terrestre comme une souffrance «exilé sur le sol au milieu des huées». «Exilé» prouve que le poète ne se trouve pas à sa place
                parmis les hommes et se sent à part.
            </li>
            <br><br>
            <li>Il est incompris et moqué comme le montre «huée». C'est sa superiorité même qui le rend inadaptée à la vie sociale.</li>
        </ul>
        </div>
        <h9 class="sous_titre" id="Conclusion">Conclusion</h9>
        <div class="content">
            Ce poème met en valeur l'inadaptation du poète à la vie quotidienne
            Son élément est le monde de l'art et de la beauté qui lui donne sa superiorité.
            Sur terre, ce monde n'est pas compris, l'albatros comme le poète passe donc de l'or à la boue
            lorsqu'ils sont avec les autres. Ce poème fait echo au premier poème du recueil en particulier 
            la 8 ème strophe qui exprime l'aggresivité des hommes. Baudelaire est sûr de sa place auprès de Dieu.<br><br>
