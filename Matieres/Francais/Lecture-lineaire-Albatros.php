        <h2 class="sous_titre" id="Intro"> Introduction </h2>
        <div class="content">
        Baudelaire est un poète du 19 ème siècle. Ce fut un poète maudit mais aujourd'hui est considéré comme l'un des meilleurs.
        Il expérimente en passant du romantisme, au mouvement parnassien, puis en insufflant le symbolisme. <br>Il se définit comme un poète de la modernité.<br><br>
        Ce poème fait parti de la section «spleen idéal» du recueil « Les fleurs du mal ». Il reprend le thème du poète maudità partir d'un apologque 
        où l'albatros devient le symbole du poète inadaptée à la vie quotidienne.
            </div>
            <h2 class="sous_titre" id="plan"> Plan </h2>
        <div class="content">
            Il s'agit d'un poème en Alexandrin avec 4 quatrain.
            Le texte est composé de deux mouvements : 3 strophes évoquant l'albatros et la dernière, expliquant le sens métaphorique du récit.<br><br>
