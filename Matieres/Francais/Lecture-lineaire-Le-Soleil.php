        <h4 class="sous_titre" id="problematique"> Problématique </h4>
        <div class="content">
        <strong>En quoi le soleil et le poète sont-ils des alchimistes ?</strong> 
        </div>
        <h5 class="sous_titre" id="1er"> 1ère strophe </h5>
        <div class="content">
            <ul class="list">

            
            <li>La rime «masures/luxures» souligne l'alliance de la misère et du vice dans la ville moderne.</li><br>
            <li>Le verbe «pendre» suggère la langueur et l'ennui dans une ville étouffante.</li><br>
            <li>Le soleil s'en prend au mal : «Quand le soleil curel frappe à traits redoublés/ sur la villes et les champs, sur  les toits et les blés».
                Cela montre la puissance et l'universalité du soleil.
            </li><br>
            <li>Le soleil exerce une aggréssivité visible avec l'allitération en 'r'.</li><br>
            <li>Le poète intervient par la première personne. L'adjectif "seul" qui renvoie à l'idée de la différence.</li><br>
            <li>L'activité du poète est désigné par une métapohre avec l'escrime qui montre qu'écrire est une lutte.</li><br>
            <li>Les trois participe présent : "flairant, trébuchant, heurtant" décrivent la poèsie comme une activité difficile.
                <br>flairant animalise le poète qui fait preuve d'auto-dérision pour montrer sa recherche incéssante "dans tout les coins" (Baudelaire ne s'interdit aucun sujet).
                trébuchant et heurtant sont là pour insister sur les erreurs possibles avant d'atteindre la perfection. Cela demande du temps pour l'auteur.
                <br>
            </li><br>

            </ul>
        </div>
        <h6 class="sous_titre" id="2eme"> 2ème strophe </h6>
        <div class="content">
            <ul class="list">
                <li>Le soleil est personnifié et divinisé par la métaphore «Ce père nourricier» qui renvoie au père de la trinité chrétienne.</li><br>
                <li>Il guerit ainsi des maladies et de la faiblesse «ennemi des chloroses». C'est un bienfait physique.</li><br>
                <li>L'alchimie poétique apparaît bien à la rime qui oppose la chlo[rose] et la rose.</li><br>
                <li>Le mot "vers" est ambigüe car il peut vouloir dire le vers de terre mais aussi le vers poétique.</li><br>
                <li>Le soleil est en antidote au spleen «il fait évaporer les soucis vers le ciel».</li><br>
                <li>Le verbe remplit suggère l'abondance produit pas le soleil. Il stimule la création « il fil remplit les cerveaux ».</li><br>
                <li>Le soleil est aussi utile à la vie «remplit les ruche de miel», «commande aux moissons de croître». Le miel a la couleur de l'or..</li>
                <li>Baudelaire continue ses énumérations sur les bienfaits du soleil : «C'est qui lui  rajeunit les porteurs de béquilles»</li>
                <li>Le poète a pour ambition l'immortalité grâce à la poésie. Le verbe fleur rapelle le titre de l'oeuvre qui associe la fleur au poème.</li>
            </ul>
        </div>
        <h7 class="sous_titre" id="3eme"> 3ème strophe </h7>
        <div class="content">
            <ul class="list">
               <li>Baudelaire établie une comparaison explicite entre le soleil et le poète grâce à la reprise de quand et à l'outil de comparaison : «ainsi que».</li>
                <li>Le poète est d'après lui comme le soleil qui descend en roi pour transformer le mal en or (le vile en rime).</li>
                <li>La double négations suggère la discrétion et la solitude mais à la fois la puissance du Poète.</li>
            </ul>
        </div>
        <h8 class="sous_titre" id="Conclusion"> Conclusion </h8>
        <div class="content">
           Pour conclure le soleil est dans ce poème le guerrisseur de toute chose, de toute maladie même du Spleen.
           Si le titre du poème semble être le thème principale, c'est en réalité le poète et la conception de la poésie qui sont mis en valeur. 
           Ce poème montre bien les caractéristiques de l'imaginaire Baudelaire qui dans un cadre lié à la boue, parvient à transformer le réel en or.
