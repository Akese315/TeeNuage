        <h2 class="sous_titre" id="Intro"> Introduction </h2>
        <div class="content">
        Baudelaire est un poète du 19 ème siècle. Ce fut un poète maudit mais aujourd'hui est considéré comme l'un des meilleurs.
        Il expérimente en passant du romantisme, au mouvement parnassien, puis en insufflant le symbolisme. <br>Il se définit comme un poète de la modernité.<br><br>
            Le soleil est le deuxième poème de la section tableau parisien. Consacré à l'évocation de la ville comme solution possible au spleen. 
            Ce poème évoque les bienfaits du soleil lié pour Baudelaire à l'idéal et donc à l'or.
            Nous verrons comment un cadre urbain relevant de la boue est transformé en or grâce au soleil au poème.<br><br>
            Le poème présenté est composé de 3 strophes de respectivement 8,8 et 4 vers en alexandrins.

        </div>
        <h3 class="sous_titre" id="plan"> Le plan </h3>
        <div class="content">
            La première strophe raconte la déambulation du poète dans la ville.
            La balade créative dans une ville délabrée.
            <br><br>
            La deuxième strophe est un éloge du soleil. Le rôle qu'il joue sur les hommes et sur la terre.
            <br><br>
            La troisième strophe est une analogie entre le poète et le soleil.
            <br><br>
