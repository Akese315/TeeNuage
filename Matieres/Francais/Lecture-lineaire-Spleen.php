        <h2 class="sous_titre" id="Intro"> Introduction </h2>
        <div class="content">
        Baudelaire est un poète du 19 ème siècle. Ce fut un poète maudit mais aujourd'hui est considéré comme l'un des meilleurs.
        Il expérimente en passant du romantisme, au mouvement parnassien, puis en insufflant le symbolisme. <br>Il se définit comme un poète de la modernité.<br><br>
             Spleen LXXVIII est un poème issu de la section « Spleen et idéal » écrit par Charles Baudelaire.
             Le poète nous immerge dans un moment de sa vie douloureux, dépressif où il se sent emprisonné 
             par son environnement jusqu’à succomber au désespoir. 
        </div>
        <h3 class="sous_titre" id="plan">Le plan</h3>
        <div class="content">
            Le poème est divisé en deux mouvements :
            <ul class="list">
                <li>Les 3 premières strophes décrivent le spleen</li><br>
                <li>Les 2 dernières décrivent la victoire du spleen sur lel poète mais aussi
                    comment le poète arrive à s'éloigner.
                </li>
            </ul>
