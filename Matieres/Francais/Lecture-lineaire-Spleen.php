        <h4 class="sous_titre" id="problematique">La problématique</h4>
        <div class="content">
            Nous verrons comment Baudelaire transforme l'ennui en or.
        </div>
        <h5 class="sous_titre" id="plan">1ère strophe : Un sentiment d'oppression</h5>
        <div class="content">
            <ul class="list">
                <li>L'anaphore et la conjonction de subordination : « quand » rappelle que le monde
                    est soumis à un temps pesant et destructeur.
                </li><br>
                <li>Cette pesanteur est emplifiée par le champs lexical du poid : « Lourd », 
                « pèse », « couvercle », « bas ». Le poète ne peut pas s'élever il est prisonnier.

                </li><br>
                <li>
                    Le poète exprime sa souffrance avec « Esprit gémissant ». 
                    Le participe présent gémissant souligne l'absence de force pour surmonter le spleen.
                </li><br>
                <li>
                    Tout oppresse le poète ainsi le champ lexical de la liberté : « ciel », « esprit »,
                    « horizons », « jours » sont en antithèse avec « bas », « ennuis », « couvercle », « nuit ».
                    L'ennui provoque à la fois le Spleen. Le cercle empêche l'élévation. Et la nuit empêche la lumière de l'atteindre.
                    Le couvercle représente pour lui l'oppression.
                </li><br>
            </ul>
        </div>
        <h6 class="sous_titre" id="2eme"> 2 ème et 3 ème strophe : Effacement du poète</h6>
        <div class="content">
               <ul class="list">
                   <li>L'auteur se sent emprisoné comme le montre son champs lexical :
                   « cachot », « mur »,  « plafonds », « prison », « prison ». Le monde devient
                ainsi une prison angoissante et sans issue.</li><br>
                <li>
                    Le termes  « espérance » est une allégorie pour l'idéal. Cependant elle 
                    est comparé à une chauve souris. Un animal vivant dans l'obscurité et l'humidité de la grotte
                    qui crée une atmosphère oppressante. Il réprésente pour Baudelaire l'impossiblité d'échapper au Spleen.

                </li><br>
                <li>
                    Le participe présent  « Battant » insiste sur la lourdeur et la durée de l'action de 
                    la chauve-souris qui tourne sans trouver d'issus.
                </li><br>
                <li>La pluie est associée au Spleen. Elle est comparée à un cachot qui forme des barreaux sans fin. </li><br>
                <li>Un nouvel animal à la conotation péjorative participe à l'enfèrmement : l'araignée.
                    Le mot filet donne une impression de piège qui prend l'esprit du poète et du lecteur :
                    « Vient tendre ses filets au fond de nos cerveaux ».
                </li>
                <li>
                   Baudelaire transmet ça plainte languissante et persistante avec l'assonnance nassale en "an" :
                    « Battant », « changée en cachot », « Espérance », « se cognant ».
                </li>
                
               </ul>
        </div>
        <h7 class=sous_titre id="4eme">4ème est 5ème strophe : Une victoire du Spleen ?</h7>
        <div class="content">
            <ul class="list">
                <li>Il y a une rupture avec le premier mouvement avec : « Tout à coups » en antithèse 
                    avec « muet ». </li><br>
                <li>Il montre de nouveau qu'il pense avoir sa place près de dieu avec le champs lexical de
                    la religion : « cloche », « ciel », « esprit ».
                </li><br>
                <li>Le champs lexical du cris renforce le sentiment d'exil que Baudelaire n'arrive pas à échapper :
                « furie », « hurlement », « affreux ».
                </li><br>
                <li>Le verbe « geindre » montre la révolte contre le spleen n'a pas aboutie. Cette plainte semble s'allonger
                    avec l'adverbe « opiniatrement ».
                </li><br>
                <li>
                    Dans le dernier quatrain, le poète raconte sa défaite face au spleen.
                    l'espoir est une allégorie de l'idéal et il est vaincu comme le montre la métaphore militaire :
                    « l'Angoisse atroce, despotique, sur mon crâne incliné plante son drapeau noir. ».
                </li><br>
                <li>L'auteur emploie la première personne pour montrer que cette défaite est personnel.</li>
                <br>
                <li>Le vocabulaire reste hyperbolique : « atroce », « despotique » pour montrer la toute
                    puissance du spleen.
                </li><br>
                <li>Le poème se termine sur un mouvement vers le bas (le spleen) : « incliné plante son drapeau noir »</li>
            </ul>
        </div>
        <h8 class="sous_titre" id="conclusion">Conclusion</h8>
        <div class="content">
        Ce poème se construit dans une progression par étapes qui part d’un environnement naturel hostile pour se rapprocher 
        du poète et de la définition de son état-d’âme. Les décors naturels tel que la pluie immerge le lecteur de sa dépression.
        Le texte apparaît en effet à la fin comme étant lyrique.
        <br><br>
        L’auteur évoque ses sensations aussi bien physiques que morales pour décrire
        le Spleen. Il apparaît comme une prison dont il est impossible de s’échapper
        <br><br>
        Ce poème constitue l’une des quatre définitions du Spleen donnée par Baudelaire. Dans chacun de ses textes, 
        il détaille de plus en plus le Spleen.
        Ici il s'agit de l’enfermement, dans le poème LXXVI sur la solitude et la longueur du temps qui passe.
