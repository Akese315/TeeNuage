        <h2 class="sous_titre" id="Intro"> Introduction </h2>
        <div class="content">
            Francis Ponge est un poète du 20<sup>ème</sup> siècle dont le projet poétique est de considérer toutes choses
            comme incconues. IL écrit des définitions-desciptions pour faire connapitre les objets tout en remettant en question les formes 
            traditionnelles du langage.
            <br><br>
            Il devient célèbre par son recueil "le parti prit des choses".            
            Ce poème fait partie du recueil "Pièces" et fait un éloge paradoxal de la boue transformée en or par l'auteur.            
            <br><br>L'ode est un genre poétique antique destiné à être chanté  pour valoriser une personne ou un évènement.
            On choisit donc une forme traditionnelle mais l'adj inachevée annonce qu'il va jouer avec les codes en  refusant une forme
            définitive et en choisissant comme thème la boue à connotation péjorative.
        </div>
