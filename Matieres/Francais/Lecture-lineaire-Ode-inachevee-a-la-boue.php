        <h5 class="sous_titre"id="plan">1er mouvement : Le mépris de la boue</h5>
        <div class="content">
            <ul class="list">
            <li>La boue plaît aux coeurs nobles parce que constamment méprisé" est une phrase provocatrice
            signifiant que le mépris général rend la boue attractive : " parce que " 
            est une conjonction de subordinnation qui exprime l'attraction. </li>
            <br>
            <li>
                L'antithèse entre "plaît" et méprisé montre une opposition entre 2 attitudes "les coeurs nobles" périphrase qui
                désigne l'élite et les autres.
    
            </li><br>
            <li>
                L'adjectif "nobles" évoque une superiorité sourde, morale et artistique
            </li>
            <br>
            <li>
                L'adverbe "constamment" et le présebt de véruté générale montrent une attitude inteporelle.
                Aimer la boue permet d'être différent des autres. Mépris du thème du poète supérieur.
            </li><br>
            <li>
                Le poète exprime le mépris de l'humanité toute entière avec les determinants possessifs "notre", "nos" et 
                le pronom indéfini "on".
            </li><br>
            <li>
                Le verbe arcaïque "honnit" suggère l'intemporalité de ce mépris. Ce mépris est depuis très longtemps pensé.
            </li>
            <br>
            <li>La boue est rejetée moralement "honnit" "esprit" et physiquement "pieds" et nous recherchons à la détruire "écrase"
                + pronoms COD "la", " l'".
            </li><br>
            <li>
                L'auteur explique ensuite les causes de notre rejet: elle nous "salit" verbe qui rappelle l'orgine de la boue : la saleté.

            </li><br>
            <li>Le pronom "elle", allitération en [r] et l'assonnance en [i] rappellent le mépris.</li><br>
            <li>"pardonne" annonce la personnification de la boue.</li><br>
            <li>"honte", "inflige", "tord" et "humiliation" rappellent l'humiliation que subit la boue.</li><br>
            <li>Rapport du sens métaphorique de la boue qui devient une insultepar le discours indirect et le "!".</li><br>
            <li>La boue sert à désigner ce qui est détesté comme l'indique "abomine" ainsi que ce qui est immortal "injures basses" qui fait
                allusion à l'expression "trainer quelqu'un dans la boue".
            </li><br>
            <li>"intéressé" signifie qu'elle recher un interêt personnel.</li><br>
            <li>La personnification montre les conséquences morales de notre attitude sur la boue amplifiées par des hyperboles 
                "du tort à jamais" "constante humiliation" et atroce et par une question réthorique :"qui la meriterait ?" nous accusant
                d'injustice pour nous faire culpabiliser.
            </li><br>
            <li>La phrase nominale "Cette atroce persévérance !" exprime la révolte de Francis Ponge face à cette injustice.
                Le parallélisme amplifie l'accusation de l'homme égoïste comme le montre : " sans soucis de la honte qu'on lui inflige".
            </li>
            </ul>
            
