        <h6 class="sous_titre"id="plan">2ème mouvement : La déclarations d'amour du poète à la boue</h6>
        <div class="content">
            <ul class="list">
                <li>Le mouvement est en antithèse avec le premier ar Ponge va proposer un plaidoyer de la boue qu'il continue à personnifier.
                </li><br>
                <li>"Je" la première personne du singlulier est pour la première fois employé dans une déclaration d'amour lyrique.</li><br>
                <li>Cette déclaration est valorisée par un chiasme fondé sur une antithèse entre "aime" et "mépris" et par l'intensif "si"
                    => le poète fait partie des "coeurs nobles".
                </li><br>
                <li>
                    Il appelle la boue à se révolter grâce à l'impératif : "jaillis à la face de tes détracteurs ! "
                    => Il permet de se venger par ce texte des détracteurs qui rabaissent la boue.

                </li><br>
                <li>
                    "Au sens propre" est un jeu de mot entre le sens du dictionnaire mais l'adjectif "propre" est eb antithèse
                    avec "sale". Ponge veut changer la perception que le lecteur a de la boue.
                </li><br>
                <li>"Tu es si belle !" est une exclamation admirative ventant la beauté de la boue inensifié par "si" en antithèse avec
                    "Tu es si méprisée".

                </li><br>
                <li>"Ailes bleues" est une métaphore sureprenante qui suggère la loyauté. "Bleu" renvoie vers au ciel qui est un mouvement vers le haut donc mélioratif en poésie.

                </li><br>
                <li>"te fonde" est la création donc l'achimie puisqu'on transforme de la boue en que chose de plus léger. Cette beauté
                    poétique est renforcée par un alexandrin blanc de "tu" à fonde suivi d'un hémistiche.
                </li>
                <br>
                <li>Le paragraphe donne un sens historique à la boue.</li><br>
                <li>"Quand" et le C.L du temps "lointain", "prochain", "longtemps" soudain annoncent cette réflexion historique.</li><br>
                <li>Les temps sombres où l'homme s'est montré violent sont évoqués par "sombre" l'allusion au supplice de la roue et 
                    aux charettes des condamnés à mort : "Charettes hostiles".
                </li><br>
                <li>La boue est représentée par un symbole d'espoir "la pluie battant" rappelle le déluge purificateur.</li><br>
                <li>"Fonde" insiste sur la force créatrice faisant naître la boue.</li><br>
                <li>Le champs lexical religieux : "pur", "adore" et ragemouillé qui évoque une posture de prière => la boue prend une dimension sacrée
                    => le poète presente la boue comme un rachat de la violence humaine.
                </li><br>
                <li>"opiniatre" et "constance" soulignent l'ostination de l'oiseau à rester attaché à =>Mare c'est-à-dire à sa patrie et ses valeurs ( liberté )
                     la sarcelle devient modèle pour nous.
                    
                </li>
            </ul>
