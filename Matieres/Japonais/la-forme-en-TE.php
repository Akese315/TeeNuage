<h2 class="sous_titre" id="intro">La fonction de la forme TE</h2>
<div class="content">
<p>
La forme TE est une des conjugaison des verbes en japonais.
Elle permet de donner un ordre (impératif), de demander quelque chose, ou encore d'exprimer le présent continu.


