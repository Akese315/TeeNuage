        <h2 id="Difference" class="sous_titre">Différence entre Jidoushi et Tadoushi</h2>
        <div class="content">
            <p>Tout d'abord il est important de comprendre la différence entre tadoushi et jidoushi qui contrairement au français nécessite une 
                modification lexicale. Par exemple :<br><br>
                <ul class="list">
                    <li>La porte se ferme <i class="fas fa-arrow-right"></i> ドアーが閉まります。</i></li>
                    <li>Je ferme la porte <i class="fas fa-arrow-right"></i> ドアーを閉めます。</i></li>
                </ul>
                Globalement lorsque le verbe est intransitif, cela signifie que le sujet subit l'action. 
                Lorsqu'il est transitif, il fait subir l'action.
            </p>
