<p style="color:red"><i class="fas fa-exclamation-triangle"></i>Par convention, tous les biens sont considérés comme marchands. Cela signifie
donc qu’il n’existe pas de biens non marchands. Nous pouvons en déduire que la
production non marchande se compose uniquement de services non marchands. </p>
<br><br>
<a class="surlignage">Le coût de production</a> représente pour un producteur l'ensemble des dépenses nécessaires à l'obtention d'une quantité donnée de produit.
<br><br><a class="fleche">Exemple :</a> pour faire une pizza, j'ai besoin de 2 tomates à 40 centimes chacune, d'un packet de fromage à 1€30 et et de la farine à 1€50.
Mon coût de production est donc l'ensemble de mes achats donc : 3€60.
</p>
