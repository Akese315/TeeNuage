<h2 class="sous_titre" id="production_def">Production au sens économique</h2>
<div class="content">
<a class="fleche">La production de richesse</a> correspond à la production de biens et de services. <br><br>

<a class="surlignage">Un bien : </a>est un produit matériel, consoomé après avoir été produit.<br><br>
<a class="surlignage">Un service : </a>est un produit immatériel, consommé en même temps qu'il est produit.




</div>
<h3 class="sous_titre"id="production_caracteristique">Caractéristique d'une production</h3>
<div class="content">
Lors de la création d'une production, plusieurs <a class="fleche">facteur de production</a> entre en jeux:
<ul class="list">
<li>Le facteur travail</li>
<li>Le facteur capital</li>
<li>Ressources naturelles</li>
</ul>
<a class="fleche">Le facteur travail</a> représente la main d'oeuvre, <a class="fleche">le facteur capital</a> correspond
 aux machines(équipements) et <a class="fleche">les ressources naturelles</a> tel l'eau, la terre ou encore le soleil utilisé pour la conception du produit.

<br><br>
Les ressources sont divisés en deux type:
<ul class="list">
<li>Bien de production durable</li>
<li>Les consommations intermédiaires</li>
</ul>
<a class="fleche">Les consommations intermédiaires</a> désignent la valeur des biens et services qui sont entièrement consommés ou transformés au cours du processus de fabrication de biens 
ou de services. <br><br><a class="surlignage">Exemple:</a> lorsque l'on cuisine une pizza, les ressources (farine, tomate, fromage, olive) vont disparaître et mélangé pour donner lieu à une pizza.
<br><br>
<a class="fleche">Biens de production durable</a> désignent généralement des machines ou des locaux car les machines continuent de fonctionner après plusieurs cycles de processus productif.

</div>
<h4 class="sous_titre" id="production_marchande_non_marchande">Production marchande et non marchande</h4>
<div class="content">
<p><a class="surlignage">Une production marchande :</a> est une production de biens ou de services dont le prix auquel il est vendu sur un marché, couvre 50% des coûts de production(significatif).
<br><br>
<a class="surlignage">Une production non-marchande :</a> est une production de biens ou de services dont le prix auquel il est vendu sur un marché, est inférieur à 50% des coûts de production(non-significatif).
<br><br>
<em>Exemple la vente de fruits et de légume est une production marchande.</em>
