<h5 class="sous_titre" id="les_entreprises">Les entreprises</h5>
<div class="content">
<p>
Les entreprises sont des organisatoins productives marchandes existe deux types d'entreprises:
<ul class="list">
<li>Les entreprises publiques</li>
<li>Les entreprises privées</li>
</ul>
<br>
On distingue deux type d'entreprises, les entreprises privées tel que Apple ou Renault et les entreprises publiques lorsque l'Etat est propriétaire de l'entreprises
à plus de 50%.
<br><br>
Une association est une forme d'entreprise privée. Néanmoins, elle se distingue des entreprises que l'on connaît car elles n'ont pas le même but/objectif.
Il existe deux but : 
<ul class="list">
<li>Le but lucratif</li>
<li>Le but non-lucratif</li>
</ul>
<a class="surlignage">Une entreprise lucratif :</a> est une entreprise dont l’objectif principal est la maximisation du profit (la
rentabilité de l’activité de production) qui servira notamment à rémunérer les
travailleurs et à investir dans l’entreprise. On recherche l’enrichissement des membres
de l’entreprise et la rentabilité.
<br><br>
<a class="surlignage">Exemple :</a><em>Samsung, est une entreprise qui cherche à s'enrichir grâce à la vente de leur produit high-tech.</em><br>
<img src="https://www.labourseauquotidien.fr/wp-content/uploads/2019/09/shutterstock_1471403240.jpg">
<br><br>
<a class="surlignage">Une entreprise non-lucratif :</a>est une entreprise dont l’objectif principal n’est pas la maximisation du
profit mais de répondre à l’intérêt général. Autrement dit, le profit créé n’a pas pour but
l’enrichissement des membres de l’entreprise (ils ne sont pas rémunérés pour ce qu’ils
produisent) et on cherche à répondre à l’intérêt du plus grand nombre même si c’est très
coûteux.
<br><br>
<a class="surlignage">Exemple :</a><em>"Les restos du coeur" est une entreprise lucratif car elle apporte de l'aide dans le domaine alimentaire aux plus démunies.</em><br>
<img src="https://upload.wikimedia.org/wikipedia/fr/thumb/a/ad/Restos_du_coeur_Logo.svg/1200px-Restos_du_coeur_Logo.svg.png">
<br><br>
</p>
