</p>
</div>

<h3 class = "sous_titre"> Les chocs</h3>
<div class ="content">
<p>

Il existe plusieurs chocs économique, qui sont en réalité une hausse ou une baisse brutale de la demande ou de l'offre.
<br><br>
<ul class = "list">
    <li>Choc de demande positif = hausse de la demande <br>-> pénurie.</li><br>
    <li>Choc d'offre positif = hausse de l'offre<br>-> surproduction.</li><br>
    <li>Choc de demande négatif = baisse de la demande<br>-> stock.</li><br>
    <li>Choc d'offre négatif = baisse de l'offre <br>-> sousproduction.</li><br>
</ul><br><br>

Les chocs crées de nouveaux prix d'équilibre.<br><br>
Exemple de chocs :
<br><br>
<img src = "images/image_marche2.png" alt = "Exemple de choc au marché" id ="image_choc">
<br><br>
Nous pouvons voir qu'un nouveau prix d'équilibre s'est formé et qu'il s'agit d'un choc de demande puisque
la courbe d'offre ne bouge pas et qu'une nouvelle courbe de demande s'est formé.<br>
De plus la nouvelle courbe se trouve audessus donc le choc est positive étant donné qu'il s'agit d'une hausse
<br><br>

<a class ="surlignage">Choc d’offre positif:</a> désigne une hausse brutale de la quantité offerte sur un marché à la
