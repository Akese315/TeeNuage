<h1 class = "titre">Le marché</h1>
<div class = "content">
<p>

<a class ="surlignage">Définition</a>
 Un marché est un lieu fictif ou réel dans lequel se rencontre une offre et une demande pour un prix donné.
 <br><br>
 <ul class ="list">
 <li>Un marché est réel quand l'échange est physique, <br> exemple
     : le supermarché, le concessionaire automobile.</li>
 <br><br>
 <li>Un marché est virtuel quand l'échange d'au moins 1 produit est numérique, exemple : Amazon, EDF (l'électricité).</li>
 <br><br>
</ul>
Pour chaque échange, il existe un demandeur et un offreur. Des biens physique ou numérique sont échangés et ils 
possèdent des valeurs (prix d'échange).
<br><br>
<a class = "surlignage">Offre:</a> quantité d’un produit que les agents économiques (= les producteurs) sont prêts à
vendre à un prix donné sur un marché.
<br><br>
<a class = "surlignage">prix : </a>Valeur d’échange d’une marchandise
<br><br>
<a class = "surlignage">demande</a>quantité d’un produit que les agents économiques (=les consommateurs) sont
prêts à acheter à un prix donné sur un marché.<br><br>
</p>
</div>
<h3 class = "sous_titre"> La loi de la demande</h3>
<div class ="content">
<p>

<a class ="surlignage">La loi de la demande :</a> dit que si le prix d'un produit augmente, alors sa demande diminue et inversement.
Car le produit étant plus cher, les agents économiques (les acheteurs) seront moins tenté de l'acheter ou réduiront sa consomation.
<br><br>Sur une réprésentation graphique, la courbe de demande (bleu) ira dans le sens contraire du prix.
<br><br>
<img src = "images/image_marche.png" alt = "Exemple de courbe au marché" id = "image_marche">
<br><br>
Sur cette image, nous pouvons remarquer sur la courbe de demande que plus le prix est bas, plus la demande est élevée.


</p>
</div>
<h3 class = "sous_titre"> La loi de l'offre</h3>
<div class ="content">
<p>
<a class ="surlignage">La loi de l'offre :</a> dit que si le prix d'un produit augmente, alors son offre aussi.
Car si le prix augmente, les producteurs y verront une source de profit supplémentaire (plus on vend, plus on fait de profit).
<br><br>Sur une réprésentation graphique, la courbe de demande ira dans le même sens du prix.
<br><br>
<img src = "images/image_marche.png" alt = "Exemple de courbe au marché" id = "image_marche">
<br><br>
Sur cette image, nous pouvons remarquer sur la courbe d'offre (rouge) que plus le prix est haut, plus l'offre est élevée.


</p>
</div>

