<div class ="content">
<p>

<a class ="surlignage">Le prix d'équilibre</a> est celui qui égalise la demande et l'offre.
Le prix d'équilibre est l'intersection des deux courbes (la demande et l'offre).
<br>
Exemple :
<br><br>
