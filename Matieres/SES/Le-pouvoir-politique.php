<h5 id="rôle-scrutin" class="sous_titre">Quel est le rôle des scrutin ?</h5>
<div class="content">
<p>
<img src="https://images.laprovence.com/media/2019/05/26/1558883340_3.png?twic=v1/dpr=2/focus=755.5x583.5/cover=1700x956"><br><br>
La France est pays démocratique, donc le peuple exerce le pouvoir. Cependant dans la plupart des démocratie, le pouvoir
est dit représentatif car le peuple nomme des électeurs et ces derniers elisent les représentant.
<br><br>
<a class="surlignage">Exemple:</a> citoyens => Maires => Généraux => Régionaux => Sénateurs
<br><br>

<ul class="list">Voici les différents types de scrutin :</a><br><br>
<li><a class="surlignage">un scrutin est direct</a> si les citoyens votent directement pour les représentants (la
plupart des élections)</li><br><br>

<li><a class="surlignage">un scrutin est indirect:</a> si les représentants ne sont pas désignés directement par les
citoyens, mais par des "grands électeurs" (par ex, pour désigner les sénateurs, ce sont
des élus locaux qui votent)</li><br><br>
<li> <a class="surlignage">Un scrutin est uninominal:</a> Lorsque l’on vote seulement pour une personne (ex
législatives, présidentielle)</li><br><br>
<li>
<a class="surlignage">Un scrutin de listes:</a> Lorsque l’on vote pour une liste de candidats (ex européennes,
municipales)
</li><br><br>
<li><a class="surlignage">Un scrutin est majoritaire</a> si seul le candidat ou la liste qui a le plus de voix est élu.</li><br><br>
<li><a class="surlignage">Un scrutin est proportionnel</a> si les sièges sont distribués proportionnellement au
nombre de voix obtenues</li><br><br>
</ul>
</p>
