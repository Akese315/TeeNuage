<h2 id="intro"class="sous_titre">Introduction</h2>
<div class = "content">
<p>
En France le pouvoir est séparé en 3 : 
<ul class="list">
<li>Le pouvoir exécutif <span class="fleche">(le pouvoir d'appliquer les lois).</span></li>
<li>Le pouvoir legislatif <span class="fleche">(le pouvoir de créer des lois).</span></li>
<li>Le pouvoir juridique <span class="fleche">(le pouvoir de juger).</span></li>
</ul>
<a class="fleche">Si tout les pouvoirs étaient entre les mains d'une seule personne</a>, alors le pouvoir serait tyrannique que cette dernière 
pourrait juger arbitrairement, choisir les lois qu'elle veut afin de s'enrichir et les faire exécuter par la police.
<br><br><a class="fleche">Le but</a> d'un politicien est de conquerir le pouvoir et de l'exercer.<br><br>

</p>
</div>
<h3 id="restitution-pouvoirs"class="sous_titre">La restitution du pouvoirs législatif et exécutif</h3>
<div class="content">
<p>
En France le pouvoir législatif est entre les mains de l'assemblée, tandis que le pouvoir exécutif est entre les mains 
du président et du gouvernement.*
<br><br>
