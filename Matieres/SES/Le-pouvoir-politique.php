<h4 id="acteurs-vie-politique" class="sous_titre">Les différents acteurs de la vie politique</h4>
<div class="content">
<p>
La vie politique est animée par différents acteurs tel que les partis politique :
    <ul class="list">Exemple :
        <li>LREM (La République En Marche)</li>
        <li>RN (Rassemblement National)</li>
        <li>LR (Les Républicains)</li>
        <li>FI (La France Insoumise)</li>

</ul>

<a class="surlignage">Partis politique :</a> organisation implantée sur l'ensemble du
 territoire dont le but est d'accéder au pouvoir politique grâce au soutien populaire.
<br><br>
Les partis jouent un rôle très importants car pour avoir une chance d'être élut aux présidentielles, il faut être investi par un parti.
Ce sont les partis qui sélectionnent les candidats, les forment, financent leur campagne et proposent un programme.
</p>
