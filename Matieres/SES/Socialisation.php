<h4 class="sous_titre">Processus de socialisation </h4>
<div class="content">
<p>
Comment la socialisation se réalise ? Et bien elle se met en place par un processus de socialisation qui se fait tout au long2ipdde notre vie.
Néanmoins, on peut en noter 2 principales :
<ul class ="list">
    <li>La primaire (5-20ans)</li><br>
    <li>La secondaire(20-30ans)</li><br>
</ul>
<br>
Il éxiste deux façon d'inculquer des valeurs et des normes :<br>
<ul class ="list">
    <li>informelle</li><br>
    <li>formelle</li><br>
</ul>
<br><br>
<a class="fleche">Socialisation formelle :</a> processus de socialisation intentionnel de la part des
agents socialisateurs.Il s'agît d'un processus de socialisation volontaire et méthodique.<br><br>
<a class="fleche">Socialisation informelle:</a> Processus de socialisation non-intentionnelle de la part
des agents socialisateurs (imitateurs / imprégnation).<br>
<br>
L'individu est inculqué de valeurs et de normes par les agents d'instances de socialisation :
Voici quelques exemple d'instances de socialisation :

<ul class ="list">
    <li>Familles => (respecter ses parents = Respect)</li><br>
    <li>école => (faire ses devoirs, être rigoureux = solidarité, politesse, rigueur</li><br>
    <li>groupes de pairs => (savoirs fairs, passion, amitié )</li><br>
    <li>Médias => (esprit critique)</li><br>
</ul>
<a class="fleche">Instance de socialisation:</a> désigne l'ensemble des groupes sociaux ou institution qui participent aux processus de socialisation de l'individu
en transmettant des normes et des valeurs.<br><br>
Ainsi, on interiorisant les normes et les valeurs de la société, un être organique deveint un être social.
Autrement dit, il devient acteur social c'est à dire un individu qui se sent membre d'une société et qui et reconnue
comme tel par les autres membres. De ce fait, via le processus de socialisation, l'individu s'intègre dans un 
ou plusieurs groupes sociaux.

