<h3 id = "Economiste" class ="sous_titre">L'économiste</h3>
<div class ="content"> 
<p>

<a class =" surlignage">L'écnomiste :</a> L'économiste est une personne spécialisée dans les sciences économiques. Il étudie les phénomènes
concernant l'échange et la production de ressources dans une société.

<br><br>
<a class ="fleche">la question qu'il se pose ==> </a> Comment <a class ="fleche" href ="#allouer">allouer<a> les ressources efficacement.
<br> <br>

<a class ="fleche">allouer :</a>  allouer: c'est attribuer/consacrer une ressource à quelqu'un ou à quelque chose.
<br><br>
<a class = "fleche">  Ressource : </a> C'est un moyen matériel dont les quantités disponibles sont limitées et dont nous avons besoin.
<br>
<a class = "surlignage"> Exemple :</a> - Le diamant est une ressource rare car elle est très demandée par les bijoutiers et elle est 
en quantité limitée.<br> 
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-Le plastique au contraire est une ressource abondante car sa production est illimitée.
<br>
<br>
<a class = "surlignage">Exemple d'enquête :</a>
<br>
<br>
<a class ="fleche">"dans le cas d'un bijoutier, spécialisé en vente de diamant"</a><br><br>
imaginons que le fournisseur de diamant soit en ruture de stock et qu'il ne pourra pas en expédier durant 3 mois.
Le bijoutier sera dans l'embarras face à sa clientèle, obliger de faire un choix entre :
<br><br>1 ==> Attendre les 3 mois afin de recevoir les diamants.
<br><br>2 ==> Commander d'autres pierres précieuses, tel que le saphyr afin de garder une activité stable.

<br>
<br>
<a class ="fleche"> Le meilleur choix est celui qui permet d'accroître la satisfaction des individus (ici la production de richesse)</a>
<br><br>
Le bijoutier doit donc prendre l'option qui va lui permettre de ne pas perdre de l'argent mais d'en gagner.
<br>
On dit qu'il va <a>allouer</a> son argent dans une des options.
<br><br>
<a class = "fleche"> Une allocation efficace des ressources fait référence à la manière dont les
 ressources rares sont utilisées et afféctées aux différents usages économiques.
 </p>
