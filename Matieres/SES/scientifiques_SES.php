<h4 id = "Sociologue" class = "sous_titre">Le sociologue </h4>
<div class = "content">
<p>

<a>Le sociologue :</a> Le sociologue désigne la science qui étudie les comportements sociaux dans une société. 
Comprendre les comportements et les relations entre les individus.
<br><br>

<a class ="fleche">Question qu'il se pose ==> </a>

Exemple d'enquête :
<br><br>
En Amerique du Nord, 40% de la population est obèse.
Le sociologue va se poser la question suivante :
"comment les ammericains se nourrisent-ils et vivent-ils ?"
<br><br>
Le sociologues va donc étudier les conditions de vie des Américains.<br>
Alors, on s'aperçoit que les conditions de vie des américains résultent en partie
de leur éducation familiale et leur environnement sociétal. L'éducation en général des individus devient la culture qui  forme une société dans une époque et lieu donné.
<br>
<br>
<a class ="fleche"> ==> le cadre spatio temporel </a>
<br>
<br>
Au 21ème sciècle, la technologie et la production de nourriture a grandement augmenté. Les fast-food rendent l'accès à la nourriture possible,
même pour les plus démunis et particulièrement en Amérique.
<br>
<br>
Le sociologue peut procéder de deux façon pour appuyer ses arguments.
<br>
<br>
<a class = "surlignage">La méthode quantitative</a>
<br>
<br>
Cette méthode s'appuie sur les données chiffrées des populations.

<a class ="surlignage"> Exemple : </a> Le nombre d'élèves fumant dans une classe de 31 personnes par le moyen d'un questionnaire 
à réponse fermée ( OUI / NON).

<a class = "surlignage">La méthode Sociologique</a>

Cette méthode observe des faits dans une société et regarde s'il y a une corrélation.<br>
Si il y'en a une alors ce lien est appelé :<a class ="fleche"> "Lien de causalité"</a>.
<br>
<br>
<a class ="fleche"> Liaison entre deux faits (corrélation simple) ou plusieurs (corrélation multiple) dans laquelle les variations 
de leurs valeurs soient toujours de même sens (corrélation positive) ou de sens opposé (corrélation négative). 
Si les deux faits n'ont pas de liens alors il y a une absence de corrélation</a>
</p>
