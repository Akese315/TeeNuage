<h1 class ="titre">Les cellules </h1>


<div class = "content">
<p>
Une cellule est une unité structurelle microscopique commune à tous les êtres vivants.<br>
Elle est constitué : <br><br>
<ul class = "list">
<li>D'une membrane</li>
<li>D'un cytoplasme</li>
<li>D'un noyau</li>
</ul>
<br>
Si tout les êtres vivants sont constitués de cellules, certains n'en possède qu'une seule "<a class ="fleche">unicellulaires</a>" 
et d'autres plusieurs "<a class ="fleche">pluricellulaires</a>".
<br><br>
<a class ="fleche">Pluricellulaires</a> : organismes constitués de nombreuses cellules.
<br>
<a class ="fleche">Unicellulaires</a> : organismes constitués d'une seule cellule.
<br><br><br>
<img src = "images/cellule2.0.png" alt = "cellule_schéma">
</p>
</div>
<h3 class = "sous_titre">Les cellules spécialisées !</h3>
<div class = "content">
<p>
Les êtres vivants ont pour fonction la nutrition(respiration et nutrition), la reproduction et la communication
 (relation avec les autres êtres vivants).
<br><br>
<ul class = "list">
<li>Les êtres vivants unicellulaires réalisent à eux seuls ces trois fonctions.</li>
<li>Les êtres vivants pluricellulaires possèdent pleins de cellules qui sont spécialisées dans une de ces fonctions.</li>
</ul>
 
 <br>    <br>    <br>   
 Lors d'une expérience sur une élodée nous avions vu qu'il y avait plusieurs cellules qui remplissaient la fonction de nutrition <br>
<a class = "surlignage"> Exemple :</a> <br><br>
 Sur l'épiderme se trouve des cellules possèdant des chloroplaste, un organite qui rend possible la photosynthèse.
 La photosynthèse permet à la plante de fabriquer de la matière organique donc de se nourir (ici la fonction de nutrition).
 <br><br> Cette expérience nous a permit de comprendre que chaque tissu possède un rôle (une fonction).

<br><br> <a class = "surlignage">Organite</a> : c'est une structure spécialisé qui assure une fonction de la cellule et se trouvant à l'intérieur de son cytoplasme.
<br><br> <a class = "surlignage">Tissu</a> : groupe de cellules semblables et spécialisées dans une fonction commune.
<br><br> <a class = "surlignage">Paroi cellulaire</a> : paroi assez rigide entourant certaines cellules végétales, 
située à l'extérieur de la membrane cellulaire, qui apporte à la cellule un soutien structurel, une protection, 
et agit comme un mécanisme de filtrage.<br><br>
<img id = "chloroplaste" src = "images/Chloroplaste.png" alt = "Chloroplaste">
</p>
</div>
<h4 class ="sous_titre">L'organisation chez l'être humain </h4>
<div class = "content">
<p>
Comme vu précédemment, les cellules spécialisées se regroupe pour former des tissus afin de remplir une des fonctions vitales.
Cependant les tissus seuls, ne peuvent pas faire grand chose alors ils s'assemblent en organe. Les organes peuvent aussi assurer 
simultanément ou alternativement plusieurs fonctions.<br><br>
<a class = "surlignage">Exemple :</a> la tige d'un rosier assure la transition de la sève mais aussi la protège avec ses piques.
<br><br>
Les organes peuvent aussi s'assembler pour former un système complexe.
<br><br> <a class = "surlignage">Exemple :</a> Le système digestif est composé :<br><br>
<ul class = "list">
<li>D'un pancréas </li>
<li>D'un foie</li>
<li>D'une vésicule biliaire</li>
<li>D'intestin (gros et grêle)</li>
<li>D'un rectum</li>
</ul><br><br>
