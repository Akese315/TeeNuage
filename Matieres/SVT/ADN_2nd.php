<p>
L’ADN, qui signifie acide désoxyribonucléique, est une macromolécule contenant l’information génétique de tout être vivant.<BR><BR>
Chacune de nos cellules contient de l’ADN, véritable carte d’identité du vivant.<br><br>
Elle est constitué de 2 chaines hélicoïdales, enroulées l'une autour de l'autre et maintenues ensemble par des liaisons d'hydrogènes. 
Chaque chaine est constituée d'une séquence de nulcéotides.
<br><br>
<a class = "surlignage">Le nucléotide est constitué :</a><br><br>
<ul class = "list">
    <li>d'un groupement de phosphate</li>
    <li>de sucre</li>
    <li>d'une des 4 bases azotées : Adénine, guanine,cytosine et thymine</li>
</ul><br><br>
Sauf que l'Adénine s'associe toujours avec la thymine et la guanine avec la cytosine.<br><br><br>
