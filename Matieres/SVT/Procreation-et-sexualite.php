<h3 class="sous_titre">Fonction double de l'ovaire et de la testicule</h3>
<div class="content">
<p>

Pour les deux gonades les fonctions sont les mêmes :
<ul class="list">
    <li>produire des gamètes mâle ou femelle (spermatozoïdes ou ovules).</li><br>
    <li>produire des hormones sexuelles necessaire au développement des caractères sexuelles secondaire.
        Chez la femme, ce sera des Oestrogènes et de la progestérone. Tandis que chez l'homme ce sera de la 
        testostérone.</li>
</ul>
</p>
<img src="images/Ovaire3.jpg" id="img3">

<p>
<ul class="list">Nous pouvons voir sur cette coupe d'une ovaire fertile.<br><br>
    <li>La production de gamètes féminines est gérée par les follicules.
         Puis quand l'ovocyte arrive à maturation, le follicule l'expulse de l'ovaire. </li><br>
    <li>Les follicules se chargent aussi de produire les hormones. Les cellules folliculaires sécretent en phase
        ovulatoire des oestrogènes. Puis quand l'ovocyte arrive à maturation et qu'il est expulsé de l'ovaire, le follicule devient ce qu'on nomme un corp jaune.
        Ce corp jaune va à son tour sécréter beaucoup de progestérone.
</li>


</p>

