On peut en conclure que selon le chromosome et la présence du gène SRY, le sexe phénotype change.
Le gène SRY est porté normalement par le chromosome Y et induit la différenciation des 
gonades en testicules. Si ce gène est absent les gonades se différencient en ovaires.


</p>
<img src="images/sexualite.png" id="img1">
<p>
Lors de la grossesse, l'embryon possède deux cannaux différent (le canal de wolff et le canal de Müller).
Ce sont réspectivement les ébauches des voies génitales masculines / féminines .
A partir de la huitième semaine de grossesse, l'embryon va garder un un des deux cannaux en fonction du gène.
Si le gène RSY est présent alors le canal de Wolff se développera tandis que l'autre disparaîtra.
Au contraire, si le gène RSY est absent alors le canal de Müller se développera tandis que l'autre disparaîtra.
Et en fonction du cannal les gonades vont se différencier soit en ovaires soit en testicules.

</p>
