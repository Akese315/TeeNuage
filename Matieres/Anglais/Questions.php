<h2 class = "sous_titre" id = "questions"> Les questions</h2>
<div class = "content">
<p>
En anglais il existe plusieurs types de questions : les questions fermés(oui/non), ouvertes ou tags.
Dans ce cours, seront expliqués ces types de questions.
</p>
</div>
<h3 class = "sous_titre" id = "questions"> Les questions fermées</h3>
<div class = "content">
<p>
Pour commencer, une question fermée ,se répond par "oui" ou par "non".
Elles se forment par : un auxiliare + un sujet + une base verbale .
<br><br>


Comment reconnaître une question fermée et y répondre?
<br><br>
Elles sont caractérisées par l'auxiliaire <a class = "fleche">"DO"</a>.
L'auxiliaire peut varier en fonction du temps et de la personne concernée :<br><br>
<ul class = "list">
<li>On utilise <a class = "fleche">Do</a> pour : "I", "YOU", "WE" et "THEY" au présent.</li>
<li>On utilise <a class = "fleche">Does</a> pour : "HE", "SHE" et "IT" au présent.</li>
<li>On utilise <a class = "fleche">Did</a> pour toute les personnes concernées et uniquement pour parler d'un fait passé.</li>
</ul>
<br><br>
Ensuite on y répond par Yes ou No suivi de la personne concernée et du même auxilaire utilisé dans la question.
<br>
Exemple : <br><br>
<ul class = "list">
<li>Do you have money in your pocket?<br> <a class = "fleche">--> </a> No <a class = "fleche">i don't </a>.</li><br>
<li>Does he win his tournament<br> <a class = "fleche">--> </a> Yes of course <a class = "fleche">he does </a>.</li><br>
<li>Did you bring beer for the party.<br> <a class = "fleche">--> </a> No, sorry <a class = "fleche">i didn't </a>.</li><br>
</ul>


</p>
</div>
<h3 class = "sous_titre" id = "questions"> Les questions ouvertes</h3>
<div class="content">
<p>

Les questions ouvertes sont des questions sont des questions dans lequel la réponse peut être développée.
Elles se forment par : un mot interrogatif + un auxiliare + un sujet + base verbale .
<br><br>
Comment les reconnaître et y répondre?
<br><br>
Ces question là, on les appelles aussi les questions WH car tout ses mots interrogatifs commencent par WH.<br> <br>
<ul class = "list">
<li> When</li><br>
<li> Where</li><br>
<li> Who</li><br>
<li> How</li><br>
<li> What</li><br>
</ul>
<br><br>
Exemples : <br><br>
<ul class = "list">
<li> When did you buy this camera ? </li><br>
<li> Where are the key ?</li><br>
<li> Who are those guys ?</li><br>
<li> How many cakes did you eat ?</li><br>
<li> What is your favorite colour ?</li><br>
</ul>
<br><br>
On y répond normalement en reprennant le sujet.
<br><br>
Exemples : <br><br>
<ul class = "list">
<li> I bought it a few days ago</li><br>
<li> They are next to the tree </li><br>
<li> They are my old friend</li><br>
<li> I ate 5 cakes in a row</li><br>
<li> My favourite colour is green</li><br>
</ul>
</p>
</div>
<h3 class = "sous_titre" id = "questions"> Les questions tags</h3>
<div class="content">
<p>

Les questions tags sont utilisés pour confirmer son affirmation.
Elles se forment de la façon suivante : l'affirmation (un sujet + base verbale ) + LE TAG ( auxiliaire + sujet).
<br><br>
Comment les reconnaître et y répondre?
<br><br>
On les reconnaît tout simplement grâce au TAG qu'il y a à la fin de la question.
<br>
Exemple :<br><br>
<ul class = "list">
<li> She is really pretty,<a class = "fleche"> isn't she</a>? </li><br>
</ul>
Et on y répond comme pour les qustion fermés.
<br>
exemple : <br><br>
<ul class = "list">
<li> She is really pretty,<a class = "fleche"> isn't she</a>?<br><br><a class = "fleche">--></a> Yes, she is. </li><br>
</ul>
</p>
