<h2 class = "sous_titre">Un mouvement</h2>
<div class ="content">
<p>
En physique, le mouvement d’un objet dont on étudie est appelé un système. Pour étudier son mouvement, on utilise un référentiel. Ainsi on peut décrire la trajectoire du système.
<br><br>
<a class ="surlignage">Système :</a>
<br><br>
Cela peut être une grenouille, une balle, ou encore une voiture.
Le système doit avoir un mouvement tandis que le référentiel doit être fixe.
<br><br>
<img src ="images/grenouille.jpg" id ="img1">
<br><br>
<a class ="surlignage">Référentiel</a>
<br><br>
Un référentiel est objet qui n’exerce pas de mouvement par rapport au système. 
<br><br>
Exemple <br><br>
-réferentiels terrestre : Maison, Arbre, sol ou encore la mer. <br><br>
Etudie tout objet ayant un mouvement la surface de la terre ou dans l’atmosphère.<br><br>
-référentiels heliocentrique : c’est un repère ayant pour origine le centre du soleil. <br><br>
Etudie les astres gravitationnant autour de la terre, exemple : la lune un vaisseau, un sattelite.<br><br>
-référentiel géocentrique : repère ayant pour origine le centre de la terre.<br><br>Etudie les mouvement des astres gravitationnant autour du soleil
<br><br>
<a class ="surlignage">Le vecteur vitesse :</a>
<br><br>
Le vecteur vitesse indique le mouvement du système :<br><br>
Direction du mouvement : exemple : une voiture qui va vers l’autoroute<br><br>
Sens du mouvement : droite gauche<br><br>
Valeur de la vitesse en m/s<br><br>
Formule pour calculer la vitesse : V = Distance / Temps<br><br>
</p>
</div>
<h3 class ="sous_titre"> La trajectoire </h3>
<div class ="content">
<p>
<br><br>
La trajectoire est l’ensemble des positions du système effectué au cours du temps.<br><br>
Exemple : <br><br>
<img src = "images/trajectoire-curviligne.png" id="img2">
<br><br>
<a class ="surlignage">La trajectoire peut être :</a><br><br>
Rectiligne si sa direction n’évolue pas. Alors la trajectoire prendra la forme d’une ligne droite.<br><br>
<ul class ="list">

<li>Si la valeur du vecteur vitesse diminue : le mouvement rectiligne du système est décéléré.</li><br><br>
<li>Si la valeur du vecteur vitesse augmente : le mouvement rectiligne du système est augmenté</li><br><br>
<li>Si la valeur du vecteur vitesse reste la même : le mouvement rectiligne du système est uniforme</li><br><br>
</ul>
Curviligne si l’un des deux facteur (direction ou la vitesse) change.<br><br>
<ul class ="list"> Elle peut être de nature :<br><br>
<li>Circulaire</li>
<li>Parabolique</li>
<li>Ou autre</li> <br><br>
</ul>
