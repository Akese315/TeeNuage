<h2 class="sous_titre" id="Constitution-de-atome">La constitution de l'atome</h2>
<div class ="content">
<p>
    Dans l'atome, il existe différents éléments :
    <br><br>    
    Il existe donc :
    <ul class ="list"><br>
        <li>L'électron (charge électrique négatif)</li><br>
        <li>Le neutron (charge électrique neutre)</li><br>
        <li>Le proton (charge électrique positive)</li><br>
    </ul>
    <br><br>
    Les protons et les neutrons sont au centre de l'atome et sont appellés noyau ou nucléons.
    Le nombre de nucléon est l'ensemble des neutrons et de protons.
    Les électrons quant à eux, se situent autour du noyau.
    <br><br>
    <img src ="images/atom.png" alt ="l'atome" id="image1">
    <br><br>
    <br>
    Un atome est électriquement neutre car il possède autant de proton que d'électron.
    Donc autant de charge positive que de charge négative.
    On peut représenter les protons comme un nombre positif et les électrons comme un nombre négatif.<br><br>
    -9 (électrons) + 9 (protons) = 0 

</p>
