<h4 class ="sous_titre" id="Les-ions-monoatomiques">Les ions monoatomiques</h4>
<div class ="content">
    <p>
        Les ions monoatomiques sont des atomes ayant perdu un électron ou plusieurs.<br><br>
        Les atomes peuvent perdre leurs électrons en envoyant un photon (la lumière) ou un électron avec assez d'énergie (en électron-Volt).
        <br><br>
        Il existe deux types d'ions :
        <br><br>
        <ul class= "list">
            <li>Les cations (ion ayant perdu un électron)</li>
            <li>Les anions (ion ayant gagné un électron)</li>
        </ul>
        <br><br>
        Lorsque qu'il n'est pas demandé de donner le nombre d'électon et de neutron, nous pouvons le noter ainsi :<br><br>
        <img src ="images/Exemple_ion.png"alt ="Exemple d'ion" id="images4">
        <br><br>
        On donne d'abord le symbole de l'atome, puis le nombre d'électron en plus.<br><br>
        Si l'atome gagne 2 électrons, alors il aura 2 charge négatives en plus donc il aura <sup>-2</sup>.<br><br>
        Au contraire si il perd 2 électrons, alors il y'aura plus de protons donc deux charges positives 
        en plus par rapport au nombre d'électrons, il aura <sup>+2</sup>.
    </p>
