<div class = "content">
<p>
<span class="toto">Dans un atome</span>, il est constitué d'un noyeau, mais aussi d'électrons.
Ces électrons sont situés dans des couches. En 2nd, nous verrons uniquement 3 couches.
Grâce à la position des électrons, nous pouvons en déduire l'atome par exemple : l'Oxygène ou l'Hydrogène.
<br><br>
Pour étudier un atome et en déduire sa configuratoin électronique, nous avons besoin du tableau periodique des élements.
Nous allons apprendre à le lire et à l'utiliser.
<br><br>
<img src ="images/Tableau-periodique.png" alt = "tableau périodique" id="image1">
<br><br>
