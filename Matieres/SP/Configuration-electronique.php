Chaque colonnes représentent le nombre d'électron de Valence mais aussi une famille, exemple : La dernière colonne désigne la famille des gaz nobles.<br><br>
Le numéro se trouvant en haut à gauche est le numéro Atomique Z donc le nombre d'électron.
<br><br><br>
<a class= "surlignage">Electron de Valence : </a>les électrons de valence sont les électrons situés sur la dernière couche, externe donc, d'un atome.
